console.log(typeof "Max"); // Returns the data type of string

console.log(typeof 123); // Returns the data type of number

console.log(typeof undefined); // Returns the data type of undefined

console.log(typeof null); // Returns the data type of object

console.log(typeof NaN); // Returns the data type of number

console.log(typeof { name: "Max", age: 50 }); // Returns the data type of object

console.log(typeof [1, 2, 3]); // Returns the data type of object
